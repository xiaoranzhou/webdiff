    /**
     * Generate document information section
     * @param {object} inputJSON - Input JSON
     * @returns {string} - HTML
     */
    generateDocumentInfo(inputJSON) {
        if (!inputJSON || !inputJSON.dmp) return '';

        const dmp = inputJSON.dmp;
        let html = '<section class="section">\n';
        html += '<h2>üìÑ Document Information</h2>\n';
        html += '<table>\n';

        if (dmp.title) {
            html += `<tr><td><strong>Title</strong></td><td>${this.escapeHtml(dmp.title)}</td></tr>\n`;
        }
        if (dmp.created) {
            html += `<tr><td><strong>Created</strong></td><td>${this.escapeHtml(dmp.created)}</td></tr>\n`;
        }
        if (dmp.modified) {
            html += `<tr><td><strong>Modified</strong></td><td>${this.escapeHtml(dmp.modified)}</td></tr>\n`;
        }
        if (dmp.contact && dmp.contact.name) {
            html += `<tr><td><strong>Contact</strong></td><td>${this.escapeHtml(dmp.contact.name)}</td></tr>\n`;
        }

        html += '</table>\n';
        html += '</section>\n';

        return html;
    },

